const getDictionary = (locale: Locale): Record<string, string> => ({
  ...englishDictionary,
  ...(locale === "en" ? {} : dictionaries[locale]),
});

const format = (template: string, params: CopyParams = {}) =>
  Object.entries(params).reduce(
    (text, [key, value]) => text.split(`{${key}}`).join(String(value)),
    template,
  );

export const isLocale = (value: string | null): value is Locale =>
  value === "zh-Hant" || value === "zh-Hans" || value === "en";

export const resolveInitialLocale = (): Locale => {
  const saved = localStorage.getItem(LOCALE_KEY);
  if (isLocale(saved)) return saved;

  const language = navigator.language.toLowerCase();
  if (language.includes("hans") || language.startsWith("zh-cn")) return "zh-Hans";
  if (language.startsWith("zh")) return "zh-Hant";
  return fallbackLocale;
};

export const t = (locale: Locale, key: string, params?: CopyParams) => {
  const dictionary = getDictionary(locale);
  return format(dictionary[key] ?? key, params);
};

