export const localizeContent = (
  content: AivatarContent,
  locale: Locale,
): AivatarContent => ({
  ...content,
  room: {
    ...content.room,
    furniture: content.room.furniture.map((item) => ({
      ...item,
      name: furnitureName(locale, item),
    })),
    floorSurfaces: content.room.floorSurfaces?.map((item) => ({
      ...item,
      name: surfaceName(locale, item),
    })),
    wallSurfaces: content.room.wallSurfaces?.map((item) => ({
      ...item,
      name: surfaceName(locale, item),
    })),
    windows: content.room.windows?.map((item) => ({
      ...item,
      name: windowName(locale, item),
    })),
  },
  itemDefinitions: content.itemDefinitions.map((item) => ({
    ...item,
    name: itemName(locale, item),
  })),
  shop: {
    ...content.shop,
    currency: t(locale, "currency.bits"),
    items: content.shop.items.map((item) => ({
      ...item,
      name: itemName(locale, item),
    })),
  },
});
